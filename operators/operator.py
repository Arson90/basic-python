# Arithmetic Operators
print(2 + 4)  # → 6
print(5 - 3)  # → 2
print(3 * 3)  # → 9
print(8 / 4)  # → 2.0
print(5 % 2)  # → 1
print(2 ** 3) # → 8 

# Logical Operators
print(10 > 5)   # → True
print(10 >= 5)  # → True
print(10 < 5)   # → False
print(10 <= 10) # → True
print(10 == 10) # → True
print(10 != 20) # → True

print((10 > 4) and (3 < 6))             # → True
print((1 == 1) and (2 > 4) or (6 < 10)) # → True
print((5 > 2) and not (3 == 3))         # → False

# Assignment Operator
numA = 5
numA += 6 # → the same like numA = numA + 6
